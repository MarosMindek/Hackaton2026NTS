  function getLang() {
    try { return localStorage.getItem(STORAGE_KEY) || DEFAULT; }
    catch (e) { return DEFAULT; }
  }
  function setLang(l) {
    try { localStorage.setItem(STORAGE_KEY, l); } catch (e) {}
    window.LANG = l;
    document.documentElement.lang = l;
    window.dispatchEvent(new CustomEvent('langchange', { detail: l }));
  }

  window.LANG = getLang();
  document.documentElement.lang = window.LANG;

  // t(en, ...vars) — `en` is the English string; we look up Slovak when LANG==='sk'.
  // Supports {0},{1}… or {name} interpolation if you pass an object.
  window.t = function t(en, vars) {
    let s = window.LANG === 'sk' ? (DICT_SK[en] || en) : en;
    if (vars && typeof vars === 'object') {
      Object.keys(vars).forEach(k => {
        s = s.split('{' + k + '}').join(vars[k]);
      });
    }
    return s;
  };
  window.setLang = setLang;
  window.getLang = getLang;
  window.I18N_SK = DICT_SK;

  // React hook (available if React is present): re-renders subscribers on language change.
  window.useLang = function useLang() {
    const React = window.React;
    if (!React) return [window.LANG, setLang];
    const [lang, set] = React.useState(window.LANG);
    React.useEffect(() => {
      const onChange = (e) => set(e.detail);
      window.addEventListener('langchange', onChange);
      return () => window.removeEventListener('langchange', onChange);
    }, []);
    return [lang, setLang];
  };
